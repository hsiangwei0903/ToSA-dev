def string_to_number(s):
    if s == 'zero':
        return '0'
    if s == 'one':
        return '1'
    if s == 'two':
        return '2'
    if s == 'three':
        return '3'
    if s == 'four':
        return '4'
    if s == 'five':
        return '5'
    if s == 'six':
        return '6'
    if s == 'seven':
        return '7'
    if s == 'eight':
        return '8'
    if s == 'nine':
        return '9'
    if s == 'ten':
        return '10'
    return s